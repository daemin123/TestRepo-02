package Ch02;

public class C06실수오차범위 {

	public static void main(String[] args) {
		System.out.println("Float : "+(0.444444444444444444444444444F)); // 소수점 8자리까지 유효 자리수
		System.out.println("Double : "+(0.7777777777777777777777777777));
		System.out.println("Double : "+(0.99999999999999999999999999999));
		System.out.println("Double : "+(0.2222222222222222222222222222)); // 소수점 15 - 18 자리까진 유효 자리수 		
	}

}
